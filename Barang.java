
public class Barang {
  private int w;
  private int v;

  public Barang(int w , int v){
    this.w = w;
    this.v = v;
  }

  public void setW(int w){
    this.w = w;
  }

  public int getW(){
    return w;
  }

  public void setV(int v){
    this.v = v;
  }

  public int getV(){
    return v;
  }


}
